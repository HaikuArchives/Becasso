void
MainWindow::MessageReceived(BMessage* message)
{
	//	printf ("MainWindow::MessageReceived\n");
	switch (message->what) {
		case 'chlp':
			//		printf ("Help message received.\n");
			// Hmmm.  This message is sometimes not delivered, it seems.
			help->setText(message->FindString("View"));
			help->Invalidate();
			break;
		case B_SIMPLE_DATA:
			be_app->PostMessage(message);
			break;
		case 0:							   // Unbelievable, but this is the message you get when
			be_app->PostMessage(message);  // dropping an icon from FileTypes.
			break;
		case 'PPNT':  // RRaster drop.
			be_app->PostMessage(message);
			break;
		case 'fixC':
		{
			BPoint pt;
			BView* aView = ChildAt(0);
			uint32 buttons;
			aView->GetMouse(&pt, &buttons);
			aView->ConvertToScreen(&pt);
			set_mouse_position(int(pt.x + .5), int(pt.y + .5));
			break;
		}
		case 'open':
			be_app->MessageReceived(message);
			break;
		case 'rgsr':
			system("NetPositive http://www.sumware.demon.nl/products/becasso/register.html &");
			break;
		default:
			inherited::MessageReceived(message);
	}
}